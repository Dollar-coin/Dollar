        strNetworkID = "main";
        consensus.nSubsidyHalvingInterval = 510000;
        consensus.BIP16Exception = uint256();
        consensus.BIP34Height = 0;
        consensus.BIP34Hash = uint256();
        consensus.BIP65Height = 0; 
